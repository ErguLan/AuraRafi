#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn config_defaults() {
        let config = AssetGenConfig::default();
        assert!(!config.enabled);
        assert_eq!(config.default_max_polygons, 500);
    }

    #[test]
    fn cache_put_get() {
        let mut cache = AssetGenCache::new(10); // 10MB
        let mesh = GeneratedMesh {
            positions: vec![0.0; 9],
            indices: vec![0, 1, 2],
            normals: vec![],
            prompt: "a cube".into(),
            timestamp: 0,
        };
        cache.put_mesh("a cube", mesh);
        assert_eq!(cache.len(), 1);
        assert!(cache.get_mesh("a cube").is_some());
        assert!(cache.get_mesh("a sphere").is_none());
    }

    #[test]
    fn cache_eviction() {
        let mut cache = AssetGenCache::new(0); // 0 MB = always evict
        cache.max_bytes = 100; // 100 bytes max
        let mesh = GeneratedMesh {
            positions: vec![0.0; 30], // 120 bytes
            indices: vec![0; 30],
            normals: vec![],
            prompt: "big".into(),
            timestamp: 0,
        };
        cache.put_mesh("first", mesh.clone());
        cache.put_mesh("second", mesh);
        // First should have been evicted.
        assert!(cache.get_mesh("first").is_none());
    }
}
