/// Serialize a schematic to a shareable compact string.
///
/// The format is RON (human-readable) which can be embedded in
/// a URL or saved as a small file. For browser/WASM targets,
/// this string can be base64-encoded into a URL parameter.
pub fn share_circuit(schematic: &Schematic) -> Result<String, String> {
    ron::ser::to_string(schematic)
        .map_err(|e| format!("Failed to serialize schematic: {}", e))
}

/// Deserialize a schematic from a shared string.
pub fn load_shared_circuit(data: &str) -> Result<Schematic, String> {
    ron::from_str(data)
        .map_err(|e| format!("Failed to deserialize schematic: {}", e))
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::schematic::Schematic;
    use glam::Vec2;

    fn sample_schematic() -> Schematic {
        let mut sch = Schematic::new("TestCircuit");
        let mut r = ElectronicComponent::resistor("10k");
        r.position = Vec2::new(100.0, 100.0);
        sch.add_component(r);

        let mut led = ElectronicComponent::led();
        led.position = Vec2::new(200.0, 100.0);
        sch.add_component(led);

        sch.add_wire(Vec2::new(120.0, 100.0), Vec2::new(180.0, 100.0), "N1");
        sch
    }

    #[test]
    fn netlist_text_export() {
        let sch = sample_schematic();
        let result = export_netlist_text(&sch);
        assert_eq!(result.extension, "net");
        assert!(result.content.contains("R1"));
        assert!(result.content.contains("D1"));
        assert!(result.content.contains(".COMPONENTS"));
        assert!(result.content.contains(".NETS"));
        assert!(result.content.contains(".END"));
    }

    #[test]
    fn bom_csv_export() {
        let sch = sample_schematic();
        let result = export_bom_csv(&sch);
        assert_eq!(result.extension, "csv");
        assert!(result.content.contains("Designator,Value,Footprint"));
        assert!(result.content.contains("10k"));
        assert!(result.content.contains("LED"));
    }

    #[test]
    fn svg_export() {
        let sch = sample_schematic();
        let result = export_svg(&sch);
        assert_eq!(result.extension, "svg");
        assert!(result.content.contains("<svg"));
        assert!(result.content.contains("</svg>"));
        assert!(result.content.contains("R1"));
    }

    #[test]
    fn empty_schematic_exports() {
        let sch = Schematic::new("Empty");
        let nl = export_netlist_text(&sch);
        assert!(nl.content.contains("Components: 0"));
        let bom = export_bom_csv(&sch);
        assert!(bom.content.contains("Designator"));
        let svg = export_svg(&sch);
        assert!(svg.content.contains("<svg"));
    }
}
